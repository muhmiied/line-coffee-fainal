function validate({ source, catalogCategories, catalogProducts, sql }) {
  const errors = [];
  const productSlugs = catalogProducts.map((product) => product.slug);
  const categorySlugs = catalogCategories.map((category) => category.slug);
  const duplicateProductSlugs = uniqueDuplicates(productSlugs);
  const duplicateCategorySlugs = uniqueDuplicates(categorySlugs);
  const productsMissing1kg = catalogProducts
    .filter((product) => !product.sizes.some((size) => size.label === "1kg"))
    .map((product) => product.slug);
  const variants = catalogProducts.flatMap((product) => product.sizes);
  const blendTotals = catalogProducts
    .filter((product) => product.blend)
    .map((product) => ({
      slug: product.slug,
      total: product.blend.reduce((sum, component) => sum + component.pct, 0),
    }));
  const invalidBlendTotals = blendTotals.filter((blend) => blend.total !== 100);
  const invalidVariantSizes = variants.filter((variant) => !sizeSortOrder.has(variant.label));
  const salePriceConflicts = catalogProducts
    .filter((product) => {
      const price500 = product.sizes.find((size) => size.label === "500g")?.salePrice;
      return product.salePricePerKg !== price500 * 2;
    })
    .map((product) => product.slug);

  if (catalogCategories.length !== 7) errors.push(`Expected 7 categories, got ${catalogCategories.length}.`);
  if (catalogProducts.length !== 125) errors.push(`Expected 125 products, got ${catalogProducts.length}.`);
  if (variants.length !== 375) errors.push(`Expected 375 variants, got ${variants.length}.`);
  if (siteSettings.length !== 3) errors.push(`Expected 3 site_settings, got ${siteSettings.length}.`);
  if (duplicateProductSlugs.length) errors.push(`Duplicate product slugs: ${duplicateProductSlugs.join(", ")}`);
  if (duplicateCategorySlugs.length) errors.push(`Duplicate category slugs: ${duplicateCategorySlugs.join(", ")}`);
  if (productsMissing1kg.length) errors.push(`Products missing 1kg: ${productsMissing1kg.join(", ")}`);
  if (invalidBlendTotals.length) {
    errors.push(`Invalid blend totals: ${invalidBlendTotals.map((item) => `${item.slug}=${item.total}`).join(", ")}`);
  }
  if (invalidVariantSizes.length) {
    errors.push(`Invalid variant sizes: ${invalidVariantSizes.map((variant) => variant.label).join(", ")}`);
  }
  if (salePriceConflicts.length) errors.push(`salePricePerKg conflicts: ${salePriceConflicts.join(", ")}`);
  if (/vilaella/i.test(source)) errors.push("Catalog still contains vilaella.");
  if (source.includes("لونس")) errors.push("Catalog still contains لونس.");
  if (source.includes("كوفي ميكس اورجينال")) errors.push("Catalog still contains كوفي ميكس اورجينال.");
  if (/\/assets\//.test(sql)) errors.push("Generated seed contains placeholder asset paths.");

  const forbiddenInsertTargets = [
    "customers",
    "customer_addresses",
    "orders",
    "order_items",
    "order_status_events",
    "admin_users",
  ];
  for (const target of forbiddenInsertTargets) {
    const pattern = new RegExp(`insert\\s+into\\s+public\\.${target}\\b`, "i");
    if (pattern.test(sql)) errors.push(`Generated seed inserts forbidden table: ${target}`);
  }

  const counts = {
    categories: catalogCategories.length,
    products: catalogProducts.length,
    variants: variants.length,
    productsWith250g: catalogProducts.filter((product) => product.sizes.some((size) => size.label === "250g")).length,
    productsWith500g: catalogProducts.filter((product) => product.sizes.some((size) => size.label === "500g")).length,
    productsWith1kg: catalogProducts.filter((product) => product.sizes.some((size) => size.label === "1kg")).length,
    siteSettings: siteSettings.length,
    duplicateProductSlugs: duplicateProductSlugs.length,
    duplicateCategorySlugs: duplicateCategorySlugs.length,
    productsMissing1kg: productsMissing1kg.length,
    blendProducts: blendTotals.length,
    invalidBlendTotals: invalidBlendTotals.length,
    forbiddenOperationalInserts: errors.filter((error) => error.includes("forbidden table")).length,
  };

  return { counts, errors };
}

function main() {
  const args = new Set(process.argv.slice(2));
  const catalog = loadCatalog();
  const sql = buildSql(catalog.catalogCategories, catalog.catalogProducts);
  const validation = validate({ ...catalog, sql });

  if (validation.errors.length) {
    console.error(JSON.stringify(validation, null, 2));
    process.exitCode = 1;
    return;
  }

  if (args.has("--check")) {
    const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
    if (existing !== sql) {
      console.error(`Generated SQL differs from ${path.relative(repoRoot, outputPath)}.`);
      process.exitCode = 1;
      return;
    }
  } else if (args.has("--stdout")) {
    process.stdout.write(sql);
  } else {
    fs.writeFileSync(outputPath, sql, "utf8");
  }

  console.log(JSON.stringify(validation.counts, null, 2));
}

main();
